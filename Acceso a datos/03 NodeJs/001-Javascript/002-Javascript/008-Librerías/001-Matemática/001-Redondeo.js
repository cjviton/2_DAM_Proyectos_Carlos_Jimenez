
//El objeto Math tiene una librería matemática con muchos métodos y propiedades 


var nota = 4.6;  //En javascript el decimal es el punto
console.log(Math.round(nota))  //Al hacer un math.round hace un redondeo de la variable nota al entero más próximo
console.log(Math.ceil(nota))   //Ceil hace un redondeo a al alza
console.log(Math.floor(nota))  //Ceil hace un redondeo a la baja