import math #la librería Math proporciona funciones matemáticas


print("La raiz cuadrada de 9 es: ")
print(math.sqrt(9))          #sqrt, raix cuadrada de ..

print("El redondeo al alza de 3.3 es: ")
print(math.ceil(3.3))        #ceil, redondeo al alza

print("El redondeo a la baja de 3.3 es:")
print(math.floor(3.3))       #ceil, redondeo a la baja

print(math.sin(math.pi/2))    


